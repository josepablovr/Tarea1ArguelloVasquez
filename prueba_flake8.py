# Descripcion: funcion que realiza la suma de dos números
# Entrada: dos números
# Salida: la suma de ambos números
def sumar(a, b):
    c = a + b  # almacena el resultado de la suma en una variable
    return c  # retorna el resultado de la suma


print(sumar(2, 5))  # llama a la función sumar e imprime el resultado
